/**
* op_add - adds a to b
* @a: int
* @b: int
* Return: value
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - subtracts
* @a: int
* @b: int
* Return: value
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - multiplies
* @a: int
* @b: int
* Return: value
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - divides
* @a: int
* @b: int
* Return: value
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - modulos
* @a: int
* @b: int
* Return: value
*/
int op_mod(int a, int b)
{
return (a % b);
}
